        System.out.println("17/8+9%5-3*2 = " + (17/8+9%5-3*2));
        System.out.println("17/(8+9)%(5-3)*2 = " + (17/(8+9)%(5-3)*2));
        System.out.println("12/(2*8%4) = " + (12/(2*8%4 + 1)));
